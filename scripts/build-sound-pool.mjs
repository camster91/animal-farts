// v25s: scans public/sounds/ and writes src/audio/soundPool.ts with
// every .mp3 path. Run before vite build (wired into the build script
// in package.json). The file is generated so we don't have to maintain
// the list by hand.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..", "public", "sounds");
const OUT = path.join(__dirname, "..", "src", "audio", "soundPool.ts");

const all = [];
walk(ROOT, all);
function walk(dir, out) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, out);
    else if (entry.isFile() && entry.name.endsWith(".mp3")) {
      const rel = path.relative(ROOT, full).split(path.sep).join("/");
      out.push("/sounds/" + rel);
    }
  }
}
all.sort();

const groups = { animal: [], catalog: [], bubbly: [], dry: [], echo: [], long: [], squeaky: [], wet: [] };
for (const s of all) {
  const parts = s.split("/");
  if (parts.length === 3 && parts[2].endsWith(".mp3")) groups.animal.push(s);
  else if (parts.length === 4 && parts[2] === "extra") groups.animal.push(s);
  else if (parts.length === 4 && parts[2] === "farts") groups.catalog.push(s);
  else if (parts.length === 5 && parts[2] === "farts") {
    const flavor = parts[3];
    if (groups[flavor]) groups[flavor].push(s);
  }
}

const quoted = (s) => JSON.stringify(s);
const ts = `// AUTO-GENERATED by scripts/build-sound-pool.mjs at build time.
// v25s: master pool of ${all.length} sound files on disk. Each emoji tile
// in the grid picks a UNIQUE sound from this list. Tapping the same
// tile always plays the same sound (no rotation). Tapping two
// different emojis always plays two different sounds.

export const SOUND_POOL: readonly string[] = [
${all.map(quoted).join(",\n")}
];

// Grouped by source for optional flavor filters / Kids Mode gating.
export const SOUND_POOL_BY_GROUP: Record<string, readonly string[]> = {
${Object.keys(groups).sort().map((k) => `  ${quoted(k)}: [\n${groups[k].map(quoted).join(",\n")}\n  ]`).join(",\n")}
};
`;
fs.writeFileSync(OUT, ts);
console.log(`[build-sound-pool] ${all.length} sounds → ${path.relative(process.cwd(), OUT)}`);
